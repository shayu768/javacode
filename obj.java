//栈、堆、方法区
//栈：储存你所定义的类的地址。负责调用堆和方法区
//堆：主要是来存储数据，栈所储存的地址也是堆的地址
//    基本数据类型会直接存在堆里，字符串等会存储在方法区的存储地址
//方法区：常量池和加载类信息
//       常量池：有非基本数据类型的数据
//       加载类信息：属性信息和方法信息
//       （属性也叫 成员变量 或 field）

